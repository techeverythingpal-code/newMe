CREATE UNIQUE INDEX "teacher_infos_pkey" ON "teacher_infos" ("id");
CREATE UNIQUE INDEX "teacher_infos_teacher_id_unique" ON "teacher_infos" ("Teacher_id");
CREATE UNIQUE INDEX "teacher_justifications_pkey" ON "teacher_justifications" ("teacher_id");
